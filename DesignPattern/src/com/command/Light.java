package com.command;

public class Light {
	public void turnOn() {
		System.out.println("Light is turned on");
	}
	
	public void turnOff() {
		System.out.println("Light is turned off");
	}
	
	public void dimUp() {
		System.out.println("Light is dimmed up");
	}
	
	public void dimDown() {
		System.out.println("Light is dimmed down");
	}
}
